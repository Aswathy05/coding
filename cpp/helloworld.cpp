#include <iostream>  //is a header file library that lets us work with input and output objects such as cout
using namespace std; // means that we can use names for objects and variables from the standard library.
int main()           // a function
{
    cout << "Hello World!\n";            //(cout is an object) (<< is an insertion operator) (\n is to add a newline) (adding another \n will create a blank line)
    cout << "I am learning C++" << endl; // endl -----> another way to add a new line
    cout << "I have added a new line using another way";
    return 0; // ends the main function.
}